# 2. Library vs Framework
# Library -> Collection of functions (you call it). Ex: NumPy, Pandas
# Framework -> Provides structure, it calls your code. Ex: Django, Flask

# 3. Interpreter vs Compiler
# Interpreter -> Executes line by line (Python, JS)
# Compiler -> Translates whole code at once (C, C++)

# 4. NumPy & Pandas
import numpy as np
import pandas as pd

arr = np.array([1, 2, 3])
print("NumPy Mean:", np.mean(arr))   # NumPy Example

data = {"Name": ["A", "B"], "Age": [20, 25]}
df = pd.DataFrame(data)
print("Pandas Avg Age:", df["Age"].mean())  # Pandas Example

