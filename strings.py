# Strings are sequences of characters enclosed in quotes.
# They support indexing, slicing, concatenation, and repetition.

text = "Hello Python"

# Indexing
print("First char:", text[0])
print("Last char:", text[-1])

# Slicing
print("First 5 chars:", text[:5])
print("From index 6:", text[6:])

# Concatenation
greet = "Hello" + " " + "World"
print("Concatenated:", greet)

# Repetition
print("Repetition:", "Hi! " * 3)

# Length
print("Length:", len(text))
